/** Returns the primary country plus all countries it manages. */
export function getCountriesForRep(primaryCountry: string): string[] {
  const managed = ALL_COUNTRIES
    .filter(c => c.managedBy === primaryCountry)
    .map(c => c.name);
  return [primaryCountry, ...managed];
}

/** Returns countries where managedBy is null, plus the three special offices. */
export function getIndependentCountries(): string[] {
  const independent = ALL_COUNTRIES
    .filter(c => c.managedBy === null)
    .map(c => c.name);
  return [...independent, ...SPECIAL_OFFICES];
}

/** Returns all 213 country names. */
export function getAllCountryNames(): string[] {
  return ALL_COUNTRIES.map(c => c.name);
}

/** Returns the continent for a given country name, or empty string if not found. */
export function getContinentForCountry(name: string): string {
  return ALL_COUNTRIES.find(c => c.name === name)?.continent ?? '';
}
